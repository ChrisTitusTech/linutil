## 0. Flash ISO to USB

### Find your USB device

```bash
# Run BEFORE inserting USB
lsblk

# Insert USB, run again — the new device is your USB
lsblk

# Usually /dev/sdb, /dev/sdc, etc.
# TRIPLE CHECK — wrong device = wiped drive!
```

### Write the ISO

```bash
# Unmount if auto-mounted
sudo umount /dev/sdX*

# Write ISO (replace sdX with YOUR device, e.g., sdb)
sudo dd if=nixos-*.iso of=/dev/sdX bs=4M conv=fsync status=progress

# Sync and eject
sync
sudo eject /dev/sdX
```

> ⚠️ **WARNING**: Use `/dev/sdX` (whole disk), NOT `/dev/sdX1` (partition)

### Alternative tools

- **Ventoy** — Multi-ISO USB (just copy ISOs to the drive)
- **Popsicle** — GTK USB flasher (Pop!_OS)
- **GNOME Disks** — "Restore Disk Image" option
- **Etcher** — Cross-platform GUI

### Boot from USB

1. Insert USB into target machine
2. Reboot and enter BIOS/UEFI (usually F2, F12, Del, or Esc)
3. Select USB as boot device
4. Save and exit

Common boot keys:
| Brand | Key |
|-------|-----|
| Dell/Lenovo | F12 |
| ASUS | F8 or Esc |
| HP | F9 or Esc |
| Acer | F12 |
| MSI | F11 |
| Gigabyte | F12 |

