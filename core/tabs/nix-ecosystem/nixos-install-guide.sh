## Addendum: GNU Guix System Installation

> GNU Guix is a functional package manager and libre operating system using
> Guile Scheme for configuration. Similar philosophy to NixOS but with a
> focus on software freedom. This section covers installation on foreign
> Linux distributions and the standalone Guix System.

### Package Manager (on existing Linux — like Nix on non-NixOS)

The official install script auto-detects your architecture, sets up the
daemon, build users, and substitute authorization:

```bash
# Download and run the official installer (requires root, wget, bash, gpg)
cd /tmp
wget https://guix.gnu.org/install.sh
chmod +x install.sh
sudo bash install.sh

# After install, log out and back in, then:
guix pull                     # Update Guix itself
guix install hello            # Test with a package
```

Install instructions: https://guix.gnu.org/manual/1.5.0/en/html_node/Binary-Installation.html

### Pinned Version (for reproducibility)

Download a specific binary tarball and pass it to the install script
via the `GUIX_BINARY_FILE_NAME` environment variable:

```bash
# Download the 1.5.0 binary tarball for your arch
wget https://ftpmirror.gnu.org/gnu/guix/guix-binary-1.5.0.x86_64-linux.tar.xz
# Also grab the signature
wget https://ftpmirror.gnu.org/gnu/guix/guix-binary-1.5.0.x86_64-linux.tar.xz.sig
gpg --verify guix-binary-1.5.0.x86_64-linux.tar.xz.sig

# Pass it to the installer
cd /tmp
wget https://guix.gnu.org/install.sh
chmod +x install.sh
sudo GUIX_BINARY_FILE_NAME=/path/to/guix-binary-1.5.0.x86_64-linux.tar.xz bash install.sh
```

Binary tarball architectures (all with .sig signatures):
x86_64, i686, armhf, aarch64, powerpc64le, riscv64

### Latest (CI rolling build — x86_64 only)

Development snapshots built by Cuirass CI. No signatures available.
Download from: https://ci.guix.gnu.org

### Guix System ISO (standalone OS — like NixOS)

**WARNING: This erases everything on the target device!**

**Internet access:** Plug in an Ethernet cable. WiFi support is limited —
check compatible cards: https://guix.gnu.org/manual/devel/en/html_node/Hardware-Considerations.html

```bash
# Download ISO (replace arch as needed)
wget https://ftpmirror.gnu.org/gnu/guix/guix-system-install-1.5.0.x86_64-linux.iso
# Verify signature
wget https://ftpmirror.gnu.org/gnu/guix/guix-system-install-1.5.0.x86_64-linux.iso.sig
gpg --verify guix-system-install-1.5.0.x86_64-linux.iso.sig

# Write to USB
sudo dd if=guix-system-install-1.5.0.x86_64-linux.iso of=/dev/sdX bs=4M status=progress conv=fsync
```

ISO architectures: x86_64, i686, aarch64 (experimental/UEFI only)
AArch64 issues: https://codeberg.org/guix/guix/issues

