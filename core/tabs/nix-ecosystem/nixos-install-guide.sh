## 2. Partitioning

### Option A: Simple (ext4, single root)

```bash
# Identify your disk
lsblk

# Partition (replace /dev/sda with your disk)
sudo parted /dev/sda -- mklabel gpt
sudo parted /dev/sda -- mkpart ESP fat32 1MiB 512MiB
sudo parted /dev/sda -- set 1 esp on
sudo parted /dev/sda -- mkpart primary 512MiB 100%

# Format
sudo mkfs.fat -F32 -n BOOT /dev/sda1
sudo mkfs.ext4 -L nixos /dev/sda2

# Mount
sudo mount /dev/disk/by-label/nixos /mnt
sudo mkdir -p /mnt/boot
sudo mount /dev/disk/by-label/BOOT /mnt/boot
```

### Option B: Btrfs with subvolumes

```bash
# Partition same as above, then:
sudo mkfs.fat -F32 -n BOOT /dev/sda1
sudo mkfs.btrfs -L nixos /dev/sda2

# Create subvolumes
sudo mount /dev/disk/by-label/nixos /mnt
sudo btrfs subvolume create /mnt/@
sudo btrfs subvolume create /mnt/@home
sudo btrfs subvolume create /mnt/@nix
sudo btrfs subvolume create /mnt/@swap
sudo umount /mnt

# Mount subvolumes
sudo mount -o subvol=@,compress=zstd,noatime /dev/disk/by-label/nixos /mnt
sudo mkdir -p /mnt/{home,nix,swap,boot}
sudo mount -o subvol=@home,compress=zstd,noatime /dev/disk/by-label/nixos /mnt/home
sudo mount -o subvol=@nix,compress=zstd,noatime /dev/disk/by-label/nixos /mnt/nix
sudo mount -o subvol=@swap /dev/disk/by-label/nixos /mnt/swap
sudo mount /dev/disk/by-label/BOOT /mnt/boot

# Optional: create swapfile on btrfs
sudo btrfs filesystem mkswapfile --size 8g /mnt/swap/swapfile
sudo swapon /mnt/swap/swapfile
```

### Option C: ZFS (advanced)

```bash
# Create pool (replace disk ID)
sudo zpool create -f \
  -o ashift=12 \
  -O acltype=posixacl \
  -O compression=zstd \
  -O dnodesize=auto \
  -O normalization=formD \
  -O relatime=on \
  -O xattr=sa \
  -O mountpoint=none \
  rpool /dev/disk/by-id/YOUR-DISK-part2

# Create datasets
sudo zfs create -o mountpoint=legacy rpool/root
sudo zfs create -o mountpoint=legacy rpool/nix
sudo zfs create -o mountpoint=legacy rpool/home

# Mount
sudo mount -t zfs rpool/root /mnt
sudo mkdir -p /mnt/{nix,home,boot}
sudo mount -t zfs rpool/nix /mnt/nix
sudo mount -t zfs rpool/home /mnt/home
sudo mount /dev/disk/by-label/BOOT /mnt/boot
```

### Option D: LUKS Encryption

```bash
# Encrypt root partition
sudo cryptsetup luksFormat /dev/sda2
sudo cryptsetup open /dev/sda2 cryptroot

# Format encrypted partition
sudo mkfs.ext4 -L nixos /dev/mapper/cryptroot
# Or use btrfs/zfs on /dev/mapper/cryptroot

# Mount
sudo mount /dev/mapper/cryptroot /mnt
sudo mkdir -p /mnt/boot
sudo mount /dev/disk/by-label/BOOT /mnt/boot
```

